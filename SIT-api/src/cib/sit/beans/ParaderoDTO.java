package cib.sit.beans;

public class ParaderoDTO {
	
	private String COD_PARADERO, NOMBRE_PARADERO;
	private double LATITUD, LONGITUD;
	public String getCOD_PARADERO() {
		return COD_PARADERO;
	}
	public void setCOD_PARADERO(String cOD_PARADERO) {
		COD_PARADERO = cOD_PARADERO;
	}
	public String getNOMBRE_PARADERO() {
		return NOMBRE_PARADERO;
	}
	public void setNOMBRE_PARADERO(String nOMBRE_PARADERO) {
		NOMBRE_PARADERO = nOMBRE_PARADERO;
	}
	public double getLATITUD() {
		return LATITUD;
	}
	public void setLATITUD(double lATITUD) {
		LATITUD = lATITUD;
	}
	public double getLONGITUD() {
		return LONGITUD;
	}
	public void setLONGITUD(double lONGITUD) {
		LONGITUD = lONGITUD;
	}
	
	
	
}
