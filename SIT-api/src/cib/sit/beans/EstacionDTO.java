package cib.sit.beans;

public class EstacionDTO {
	
	private String COD_ESTACION, NOM_ESTACION, COD_TIPO_ESTACION;
	private int COD_SERV;
	public String getCOD_ESTACION() {
		return COD_ESTACION;
	}
	public void setCOD_ESTACION(String cOD_ESTACION) {
		COD_ESTACION = cOD_ESTACION;
	}
	public String getNOM_ESTACION() {
		return NOM_ESTACION;
	}
	public void setNOM_ESTACION(String nOM_ESTACION) {
		NOM_ESTACION = nOM_ESTACION;
	}
	public String getCOD_TIPO_ESTACION() {
		return COD_TIPO_ESTACION;
	}
	public void setCOD_TIPO_ESTACION(String cOD_TIPO_ESTACION) {
		COD_TIPO_ESTACION = cOD_TIPO_ESTACION;
	}
	public int getCOD_SERV() {
		return COD_SERV;
	}
	public void setCOD_SERV(int cOD_SERV) {
		COD_SERV = cOD_SERV;
	}
	
	
	
}
