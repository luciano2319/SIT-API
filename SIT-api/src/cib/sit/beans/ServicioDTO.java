package cib.sit.beans;

public class ServicioDTO {
	private int COD_SER, ESTADO_SERV;
	private String DESC_SERV, TIPO_SERV;
	public int getCOD_SER() {
		return COD_SER;
	}
	public void setCOD_SER(int cOD_SER) {
		COD_SER = cOD_SER;
	}
	public int getESTADO_SERV() {
		return ESTADO_SERV;
	}
	public void setESTADO_SERV(int eSTADO_SERV) {
		ESTADO_SERV = eSTADO_SERV;
	}
	public String getDESC_SERV() {
		return DESC_SERV;
	}
	public void setDESC_SERV(String dESC_SERV) {
		DESC_SERV = dESC_SERV;
	}
	public String getTIPO_SERV() {
		return TIPO_SERV;
	}
	public void setTIPO_SERV(String tIPO_SERV) {
		TIPO_SERV = tIPO_SERV;
	}
	
	
}
