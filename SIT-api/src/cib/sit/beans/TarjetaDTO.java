package cib.sit.beans;

public class TarjetaDTO {
	private String COD_TARJETA, PERFIL_TARJETA, FEC_EMISION, FEC_CADUCIDAD, ESTADO_TARJETA;
	private double SALDO_TARJETA;
	public String getCOD_TARJETA() {
		return COD_TARJETA;
	}
	public void setCOD_TARJETA(String cOD_TARJETA) {
		COD_TARJETA = cOD_TARJETA;
	}
	public String getPERFIL_TARJETA() {
		return PERFIL_TARJETA;
	}
	public void setPERFIL_TARJETA(String pERFIL_TARJETA) {
		PERFIL_TARJETA = pERFIL_TARJETA;
	}
	public String getFEC_EMISION() {
		return FEC_EMISION;
	}
	public void setFEC_EMISION(String fEC_EMISION) {
		FEC_EMISION = fEC_EMISION;
	}
	public String getFEC_CADUCIDAD() {
		return FEC_CADUCIDAD;
	}
	public void setFEC_CADUCIDAD(String fEC_CADUCIDAD) {
		FEC_CADUCIDAD = fEC_CADUCIDAD;
	}
	public String getESTADO_TARJETA() {
		return ESTADO_TARJETA;
	}
	public void setESTADO_TARJETA(String eSTADO_TARJETA) {
		ESTADO_TARJETA = eSTADO_TARJETA;
	}
	public double getSALDO_TARJETA() {
		return SALDO_TARJETA;
	}
	public void setSALDO_TARJETA(double sALDO_TARJETA) {
		SALDO_TARJETA = sALDO_TARJETA;
	}
	
	
}
